export interface LabelMap {
  [key: number]: string;
}

function normalizeClassifierLabel(rawLabel: string): string {
  // MobileNet class names can include synonyms (comma-separated) and spaces/hyphens.
  return rawLabel
    .toLowerCase()
    .split(",")[0]
    .trim()
    .replace(/[\s-]+/g, "_");
}

/**
 * Get label map for Food-101 dataset
 */
export function getFoodLabels(): LabelMap {
  const labels: LabelMap = {};
  FOOD101_LABELS.forEach((label, index) => {
    labels[index] = label;
  });
  return labels;
}

