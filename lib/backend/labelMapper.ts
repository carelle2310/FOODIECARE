/**
 * Convert label to display name (Title Case, spaces)
 */
export function toLabelDisplayName(label: string): string {
  return label
    .split("_")
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

/**
 * Map prediction to label
 */
export function predictToLabel(
  prediction: { classId: number | string; confidence: number },
  dataset: "food101" | "imagenet" | "mobilenet" = "food101",
): { label: string; displayName: string; confidence: number } {
  const rawLabel = getLabelForClassId(prediction.classId, dataset);

  // For ImageNet and MobileNet, map to Food-101 style labels for nutrition lookup
  const label =
    dataset === "food101" ? rawLabel : mapImageNetToFood101(rawLabel);

  return {
    label,
    displayName: rawLabel
      .split("_")
      .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
      .join(" "), // Show original label for display
    confidence: prediction.confidence,
  };
}

/**
 * Get label for a class index or name
 */
export function getLabelForClassId(
  classId: number | string,
  dataset: "food101" | "imagenet" | "mobilenet" = "food101",
): string {
  if (dataset === "mobilenet") {
    // MobileNet returns class names directly
    return typeof classId === "string" ? classId : `class_${classId}`;
  }

  const labels = dataset === "food101" ? FOOD101_LABELS : IMAGENET_LABELS;
  if (typeof classId === "string") {
    return classId; // Already a class name
  }
  return labels[classId] || `class_${classId}`;
}

/**
 * Get top K predictions as labels
 */
export function predictionsTOLabels(
  predictions: Array<{ classId: number | string; score: number }>,
  dataset: "food101" | "imagenet" | "mobilenet" = "food101",
): Array<{ label: string; displayName: string; confidence: number }> {
  return predictions.map((pred) =>
    predictToLabel({ classId: pred.classId, confidence: pred.score }, dataset),
  );
}
